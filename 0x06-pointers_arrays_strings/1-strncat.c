#include "holberton.h"

/**
 *_strncat - concatenates two strings until n character
 *@dest: destination string
 *@src: Origin string
 *Return: char string
 */

char *_strncat(char *dest, char *src, int n)
{
	int i, j;

	i = 0;
	while (dest[i] != '\0')
	{
		i++;
	}
	for (j = 0; j < n && src[j] != '\0'; j++, i++)
	{
		dest[i] = src[j];
	}
	dest[i] = '\0';
	return (dest);
}
